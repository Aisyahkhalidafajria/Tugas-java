    public void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("ID_Pinjam");
        tbl.addColumn("NIM");
        tbl.addColumn("Nama Lengkap");
        tbl.addColumn("Id_Buku");
        tbl.addColumn("Judul");
        tbl.addColumn("Tanggal Pinjem");
        tbl.addColumn("Jatuh Tempo");
        tbl.addColumn("Tanggal Kembali");
        tbl.addColumn("Telat");
        tbl.addColumn("Denda");
        table.setModel(tbl);
        try{
             Statement statement = (Statement) connect.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from kembali");
            while (res.next())
            {
                tbl.addRow(new Object []{
                    res.getString("id_pinjam"),
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("id_buku"),
                    res.getString("judul"),
                    res.getString("pinjam"),
                    res.getString("tempo"),
                    res.getString("kembali"),
                    res.getString("telat"),
                    res.getString("denda"),
                });
                table.setModel(tbl);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
        }

