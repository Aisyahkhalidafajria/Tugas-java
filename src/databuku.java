    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int i = table.getSelectedRow();
        if (i == -1){
	return;
        }
	String id_buku = (String) table.getValueAt(i, 0);
	txtid.setText(id_buku);
	String judul = (String) table.getValueAt(i, 1);
	txtjudul.setText(judul);
	String pengarang= (String) table.getValueAt(i, 2);
	txtpengarang.setText(pengarang);
	String penerbit = (String) table.getValueAt(i, 3);
	txtpenerbit.setText(penerbit);
	String kategori = (String) table.getValueAt(i, 4);
	cmbkategori.setSelectedItem(kategori);
	String stok = (String) table.getValueAt(i, 5);
	txtstok.setText(stok);  
    }//GEN-LAST:event_tableMouseClicked

