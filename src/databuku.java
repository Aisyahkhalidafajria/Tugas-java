        public void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("ID_Buku");
        tbl.addColumn("Judul");
        tbl.addColumn("Pengarang");
        tbl.addColumn("Penerbit");
        tbl.addColumn("Kategori");
        tbl.addColumn("stok");
        table.setModel(tbl);
        try{
            Statement statement = (Statement) connect.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from uas4");
            while (res.next())
            {
                tbl.addRow(new Object []{
                    res.getString("id_buku"),
                    res.getString("judul"),
                    res.getString("pengarang"),
                    res.getString("penerbit"),
                    res.getString("kategori"),
                    res.getString("stok"),
                });
                table.setModel(tbl);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

